function palabraEnIngles(palabra) {
switch(palabra) {     
case "Gato" : 
console.log("Cat"); break; 
case "Perro" :  
console.log("Dog"); break;
case "Puerta" :
console.log("Door"); break;
case "Mesa"  :
console.log("Table");break;
case "Caer"  :
console.log("Fall");break;
case "Levantarse":
console.log("get up");break; 
default: 
console.log("La palabra ingresada es incorrecta") ;
}
} ;
palabraEnIngles("Perro")



